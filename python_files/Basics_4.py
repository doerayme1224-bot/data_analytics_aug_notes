# Lists
myList = [2, 3, 5, 7, 8, 9]

# append

myList.append(78)

myList

# count
myList.count(5) # counts amount of times 5 shows up myList

myList.append(7) # uses append to add the value 7 to the end

myList

myList.count(7) # counts amount that 7 showed up in the list

myList.append('This is my string') # adds string

myList

myList.extend(['a string']) # adds another string

myList

# extend
myList.extend([4, 5]) # uses extend to add two int values into the list

myList

myList[-1] # -1 opposite of regular indexing *accesses list from end and not the start*

myList.index(4) # finds where the value 4 is in the list

myList

# pop

myList.pop() # uses pop to get rid of the last value in a list

myList.pop(5) # uses pop to get rid of the value in the 5th index