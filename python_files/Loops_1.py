# range and for loops
for i in range(10, 1, -1): # uses all `range()` criteria, start:10, stop:1, step:-1, start tells the loop which index to start at, stop tells it which index to stop at, step in this case, makes the list go in the opposite direction
  print(i)

for i in range(1, 100):
  print(i)

for i in range(1,100, 3): # uses range() criteria, starts at 1, ends at 100, and counts by three's
  print(i) # prints each value 1 to 99 and counts by three, (1, 4, 7, 10, 13, etc)

