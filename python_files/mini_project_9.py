### Display the first and last colors from a given list of colors

colors = ['red', 'blue', 'green', 'yellow', 'purple', 'orange', 'black', 'white', 'grey', 'brown']
def find_colors(lst):
  """
  Creator: Charles
  Input: A list of colors
  Output: The first and last colors from the list
  """
  print(lst[0] + ' & ' + lst[-1])

find_colors(colors)